package day1;

public class SwitchCase {
	public static void main(String[] args) {
		int expression = 2;

	    // cek expression
	    switch (expression) {
	      case 1:
	        System.out.println("Case 1");
	      case 2:
	        System.out.println("Case 2");
	      case 3:
	        System.out.println("Case 3");
	      default:
	        System.out.println("Default case");
	    }
	}

}
