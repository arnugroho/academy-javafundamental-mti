package day2.latihan.utils.models;

public class Balok {
	private Integer panjang;
	private Integer lebar;
	private Integer tinggi;
	private Integer luas;
	private Integer volume;
	public Integer getPanjang() {
		return panjang;
	}
	public void setPanjang(Integer panjang) {
		this.panjang = panjang;
	}
	public Integer getLebar() {
		return lebar;
	}
	public void setLebar(Integer lebar) {
		this.lebar = lebar;
	}
	public Integer getTinggi() {
		return tinggi;
	}
	public void setTinggi(Integer tinggi) {
		this.tinggi = tinggi;
	}
	public Integer getLuas() {
		return luas;
	}
	public void setLuas(Integer luas) {
		this.luas = luas;
	}
	public Integer getVolume() {
		return volume;
	}
	public void setVolume(Integer volume) {
		this.volume = volume;
	}
	

}
